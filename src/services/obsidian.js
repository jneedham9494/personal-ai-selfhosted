    async saveConversation(conversationData, claudeService = null) {
        const { platform, messages, topic, metadata = {} } = conversationData;

        // Generate enhanced metadata
        const actionItems = this.extractActionItems(messages);
        const mood = this.detectMood(messages);
        const category = this.categorizeConversation(topic, messages);
        const priority = this.determinePriority(messages, actionItems);

        // Generate AI-powered content
        const summary = await this.generateConversationSummary(messages, claudeService);
        const insights = await this.generateKeyInsights(messages, claudeService);

        // Generate filename
        const date = new Date().toISOString().split('T')[0];
        const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
        const sanitizedTopic = topic ? topic.replace(/[^a-zA-Z0-9\s]/g, '').replace(/\s+/g, '-') : 'conversation';
        const filename = `${date}-${sanitizedTopic}-${timestamp.split('T')[1].substring(0, 8)}.md`;

        // Format conversation content with timestamps
        const conversationContent = messages.map(msg => {
            const role = msg.role === 'user' ? '**You**' : '**AI**';
            const time = msg.timestamp ? new Date(msg.timestamp).toLocaleTimeString('en-US', {
                hour: '2-digit',
                minute: '2-digit'
            }) : '';
            return `### ${role} ${time ? `(${time})` : ''}\n\n${msg.content}\n`;
        }).join('\n');

        // Format action items
        const actionItemsFormatted = actionItems.length > 0
            ? actionItems.map(item => `- [ ] ${item}`).join('\n')
            : '- No specific action items identified';

        // Detect related projects (placeholder for now)
        const relatedProjects = metadata.relatedProjects || [];
        const relatedProjectsLinks = relatedProjects.length > 0
            ? relatedProjects.map(p => `[[Projects/${p}]]`).join(', ')
            : 'None';

        // Create tags array
        const tags = ['ai', 'conversation', platform, category, mood];
        if (priority === 'high') tags.push('priority');

        // Create frontmatter
        const frontmatter = {
            type: 'ai-conversation',
            date: new Date().toISOString(),
            platform: platform,
            topic: topic || 'General conversation',
            category: category,
            mood: mood,
            priority: priority,
            tags: tags,
            summary: summary,
            action_items: actionItems,
            related_projects: relatedProjects,
            ...metadata
        };

        // Build full content from template
        const fullContent = `---
type: ${frontmatter.type}
date: ${frontmatter.date}
platform: ${frontmatter.platform}
topic: ${frontmatter.topic}
category: ${frontmatter.category}
mood: ${frontmatter.mood}
priority: ${frontmatter.priority}
tags: [${tags.join(', ')}]
summary: "${summary}"
action_items: ${actionItems.length}
related_projects: [${relatedProjects.join(', ')}]
---

# ${topic || 'Conversation'}

> **Summary:** ${summary}

## Metadata
- **Date:** ${new Date().toLocaleDateString('en-US', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' })}
- **Platform:** ${platform}
- **Category:** #${category}
- **Mood:** ${mood}
- **Priority:** ${priority}

## Conversation

${conversationContent}

## Key Insights

${insights}

## Action Items

${actionItemsFormatted}

## Related
- **Daily Note:** [[${date}]]
- **Projects:** ${relatedProjectsLinks}

---
*Generated by Personal AI Assistant*
`;

        // Save file
        const filePath = path.join(this.conversationsFolder, filename);
        await fs.writeFile(filePath, fullContent);

        // Link to daily note
        await this.linkConversationToDailyNote(date, filename, topic, category);

        console.log(`💾 Saved conversation: ${filename}`);
        return filePath;
    }

