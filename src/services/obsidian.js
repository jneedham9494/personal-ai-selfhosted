    async getProjectOrGoal(name) {
        // Find existing project or goal file
        const sanitizedName = name.replace(/[^a-zA-Z0-9\s]/g, '').replace(/\s+/g, '-');
        const filename = `${sanitizedName}.md`;
        const filePath = path.join(this.projectsFolder, filename);

        try {
            const content = await fs.readFile(filePath, 'utf-8');
            const parsed = matter(content);
            return { path: filePath, content, frontmatter: parsed.data, body: parsed.content };
        } catch (error) {
            return null; // File doesn't exist
        }
    }

    async linkConversationToProject(projectName, conversationFilename, conversationTopic) {
        // Add conversation link to project file
        const project = await this.getProjectOrGoal(projectName);
        if (!project) {
            console.log(`⚠️ Project/Goal not found: ${projectName}`);
            return;
        }

        try {
            const conversationLink = `- [[AI-Conversations/${conversationFilename.replace('.md', '')}|${conversationTopic}]]`;

            // Check if link already exists
            if (project.content.includes(conversationLink)) {
                return; // Already linked
            }

            // Find the Related > Conversations section and add the link
            let updatedContent = project.content;

            if (updatedContent.includes('- **Conversations:**')) {
                // Add after the Conversations: line
                updatedContent = updatedContent.replace(
                    /- \*\*Conversations:\*\*/,
                    `- **Conversations:**\n  ${conversationLink}`
                );
            } else {
                // Fallback: add before the footer
                updatedContent = updatedContent.replace(
                    /---\n\*Last updated:/,
                    `\n${conversationLink}\n\n---\n*Last updated:`
                );
            }

            // Update the "Last updated" date
            const today = new Date().toISOString().split('T')[0];
            updatedContent = updatedContent.replace(
                /\*Last updated: .*\*/,
                `*Last updated: ${today}*`
            );

            // Update frontmatter to include conversation reference
            const parsed = matter(updatedContent);
            const relatedConvs = parsed.data.related_conversations || [];
            if (!relatedConvs.includes(conversationFilename)) {
                relatedConvs.push(conversationFilename);
                parsed.data.related_conversations = relatedConvs;

                // Rebuild file with updated frontmatter
                updatedContent = matter.stringify(parsed.content, parsed.data);
            }

            await fs.writeFile(project.path, updatedContent);
            console.log(`🔗 Linked conversation to project: ${projectName}`);

        } catch (error) {
            console.error('Error linking to project:', error.message);
        }
    }

    async updateProjectProgress(projectName, progress, note = '') {
        // Update progress percentage and add progress note
        const project = await this.getProjectOrGoal(projectName);
        if (!project) {
            console.log(`⚠️ Project/Goal not found: ${projectName}`);
            return;
        }

        try {
            let updatedContent = project.content;
            const today = new Date().toISOString().split('T')[0];

            // Update progress in Overview section
            updatedContent = updatedContent.replace(
                /\*\*Progress:\*\* \d+%/,
                `**Progress:** ${progress}%`
            );

            // Update frontmatter progress
            const parsed = matter(updatedContent);
            parsed.data.progress = progress;

            // Calculate status based on progress
            if (progress >= 100) {
                parsed.data.status = 'completed';
                updatedContent = updatedContent.replace(
                    /\*\*Status:\*\* [🟢🟡🔴]+ \w+/,
                    `**Status:** ✅ Completed`
                );
            } else if (progress >= 75) {
                parsed.data.status = 'active';
                updatedContent = updatedContent.replace(
                    /\*\*Status:\*\* [🟢🟡🔴✅]+ \w+/,
                    `**Status:** 🟢 Active`
                );
            } else if (progress >= 25) {
                parsed.data.status = 'active';
            } else if (progress > 0) {
                parsed.data.status = 'active';
            }

            // Add progress note if provided
            if (note) {
                const progressNote = `\n**${today}:** ${note} (${progress}% complete)`;
                updatedContent = updatedContent.replace(
                    /## Progress Notes\n/,
                    `## Progress Notes\n${progressNote}\n`
                );
            }

            // Update last modified date
            updatedContent = updatedContent.replace(
                /\*Last updated: .*\*/,
                `*Last updated: ${today}*`
            );

            // Rebuild with updated frontmatter
            updatedContent = matter.stringify(parsed.content, parsed.data);

            await fs.writeFile(project.path, updatedContent);
            console.log(`📊 Updated progress for ${projectName}: ${progress}%`);

        } catch (error) {
            console.error('Error updating progress:', error.message);
        }
    }

    async searchNotes(query) {
        console.log(`🔍 Searching notes for: "${query}"`);
        
        const results = [];
        
        try {
            // Search in conversations folder
            const files = await fs.readdir(this.conversationsFolder);
            
            for (const file of files) {
                if (!file.endsWith('.md')) continue;
                
                const filePath = path.join(this.conversationsFolder, file);
                const content = await fs.readFile(filePath, 'utf-8');
                
                if (content.toLowerCase().includes(query.toLowerCase())) {
                    const parsed = matter(content);
                    results.push({
                        file: file,
                        path: filePath,
                        frontmatter: parsed.data,
                        excerpt: this.getExcerpt(content, query)
                    });
                }
            }
            
            console.log(`📋 Found ${results.length} matching notes`);
            return results;
            
        } catch (error) {
            console.error('❌ Error searching notes:', error.message);
            return [];
        }
    }

    getExcerpt(content, query, length = 200) {
        const lowerContent = content.toLowerCase();
        const lowerQuery = query.toLowerCase();
        const index = lowerContent.indexOf(lowerQuery);
        
        if (index === -1) return content.substring(0, length) + '...';
        
        const start = Math.max(0, index - 100);
        const end = Math.min(content.length, index + length);
        
        return '...' + content.substring(start, end) + '...';
    }

